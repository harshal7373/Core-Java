package Coding_Problems;

public class PrintEvenAndOddNumbers {

	
	
	public static void main(String[] args) {
		
		for(int i=1; i<=100; i++) {
			
			if(i%2==0) {
				System.out.println(" Even Numbers: " + i);
			}
			else {
				System.out.println("Odd Numbers: " + i);
			}
			
		}
		
	}
}
