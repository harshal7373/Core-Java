
package Logical_Operators;

class Logical { 
	
	
	public static void main(String... args) {

		System.out.println(10 > 5 && 7 < 5);  //The AND operators returns true if both conditions are true
		System.out.println(10 > 5 || 7 < 5);  // The OR operators returns true if atleast one of the condition is true.

	}
}