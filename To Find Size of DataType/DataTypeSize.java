
public class DataTypeSize {

	public static void main(String[] args) {
		
		System.out.println("Size of Byte: "+Byte.BYTES);
		System.out.println("Size of Byte: "+Integer.BYTES);
		System.out.println("Size of Byte: "+Double.BYTES);
		System.out.println("Size of Byte: "+Long.BYTES);
		System.out.println("Size of Byte: "+Float.BYTES);
		System.out.println("Size of Byte: "+Character.BYTES);
		
		System.out.println("Size of Byte: "+Byte.SIZE);
	
		
	}
}
