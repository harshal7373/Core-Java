package StaticVariable;

public class StaticVariable {

	static int a1=10;
	static String s1="Harshal";
	static String s2=s1+a1;
	
	public static void main(String[] args) {
		
		
		System.out.println(StaticVariable.a1);
		System.out.println(StaticVariable.s1);
		System.out.println(StaticVariable.s2);
	}
}
