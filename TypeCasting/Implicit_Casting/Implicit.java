package Implicit_Casting;

// Class Implicit
public class Implicit {

// Main Method
	public static void main(String[] args) {

		int a = 10;
		double b = a;
		System.out.println("Double: " + b);

		int a1 = 20;
		long b1 = a1;
		System.out.println("Long: " + b1);

		int a2 = 30;
		float b2 = a2;
		System.out.println("Float: " + b2);

	}
}
